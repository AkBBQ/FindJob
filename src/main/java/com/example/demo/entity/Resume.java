package com.example.demo.entity;

public class Resume {

    private int resumeId;

    private String personalPoint;

    private String desirePosition;

    private String workExperience;

    private String projectExp;

    private String eduExperience;

    private int userId;

    private String resumePhoto;

    private String address;

    private String resumeName;

    private String resumeTel;

    public int getResumeId() {
        return resumeId;
    }

    public void setResumeId(int resumeId) {
        this.resumeId = resumeId;
    }

    public String getPersonalPoint() {
        return personalPoint;
    }

    public void setPersonalPoint(String personalPoint) {
        this.personalPoint = personalPoint;
    }

    public String getDesirePosition() {
        return desirePosition;
    }

    public void setDesirePosition(String desirePosition) {
        this.desirePosition = desirePosition;
    }

    public String getWorkExperience() {
        return workExperience;
    }

    public void setWorkExperience(String workExperience) {
        this.workExperience = workExperience;
    }

    public String getProjectExp() {
        return projectExp;
    }

    public void setProjectExp(String projectExp) {
        this.projectExp = projectExp;
    }

    public String getEduExperience() {
        return eduExperience;
    }

    public void setEduExperience(String eduExperience) {
        this.eduExperience = eduExperience;
    }

    public int getUserId() {
        return userId;
    }

    public void setUserId(int userId) {
        this.userId = userId;
    }

    public String getResumePhoto() {
        return resumePhoto;
    }

    public void setResumePhoto(String resumePhoto) {
        this.resumePhoto = resumePhoto;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getResumeName() {
        return resumeName;
    }

    public void setResumeName(String resumeName) {
        this.resumeName = resumeName;
    }

    public String getResumeTel() {
        return resumeTel;
    }

    public void setResumeTel(String resumeTel) {
        this.resumeTel = resumeTel;
    }
}
